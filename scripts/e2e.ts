export interface E2ECommandTarget {
  id: string;
  label: string;
  command: readonly string[];
  required?: readonly string[];
  forbidden?: readonly string[];
  timeoutMs?: number;
}

export interface E2EArtifactTarget {
  id: string;
  label: string;
  path: string;
  required?: readonly string[];
  forbidden?: readonly string[];
  minBytes?: number;
}

export interface E2ECommandResult extends E2ECommandTarget {
  code: number;
  passed: boolean;
  durationMs: number;
  missing: string[];
  forbiddenMatches: string[];
  outputPreview: string;
}

export interface E2EArtifactResult extends E2EArtifactTarget {
  passed: boolean;
  bytes: number;
  missing: string[];
  forbiddenMatches: string[];
  error?: string;
}

export interface E2EReport {
  passed: boolean;
  durationMs: number;
  commands: E2ECommandResult[];
  artifacts: E2EArtifactResult[];
}

