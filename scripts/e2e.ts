export async function runE2ESuite(
  options: {
    commands?: readonly E2ECommandTarget[];
    artifacts?: readonly E2EArtifactTarget[];
    cwd?: string;
  } = {},
): Promise<E2EReport> {
  const started = performance.now();
  const commands: E2ECommandResult[] = [];
  for (const target of options.commands ?? e2eCommandTargets) {
    commands.push(await runE2ECommandTarget(target, { cwd: options.cwd }));
  }
  const artifacts: E2EArtifactResult[] = [];
  for (const target of options.artifacts ?? e2eArtifactTargets) {
    artifacts.push(await inspectE2EArtifactTarget(target, { cwd: options.cwd }));
  }
  return {
    passed: commands.every((result) => result.passed) && artifacts.every((result) => result.passed),
    durationMs: Math.round(performance.now() - started),
    commands,
    artifacts,
  };
}

export async function runE2ECommandTarget(
  target: E2ECommandTarget,
  options: { cwd?: string } = {},
): Promise<E2ECommandResult> {
  const started = performance.now();
  const command = new Deno.Command(target.command[0]!, {
    args: [...target.command.slice(1)],
    cwd: options.cwd,
    stdout: "piped",
    stderr: "piped",
    signal: AbortSignal.timeout(target.timeoutMs ?? 30_000),
  });
  try {
    const output = await command.output();
    return inspectE2ECommandOutput(target, {
      code: output.code,
      stdout: new TextDecoder().decode(output.stdout),
      stderr: new TextDecoder().decode(output.stderr),
      durationMs: Math.round(performance.now() - started),
    });
  } catch (error) {
    return inspectE2ECommandOutput(target, {
      code: 1,
      stdout: "",
      stderr: error instanceof Error ? error.message : String(error),
      durationMs: Math.round(performance.now() - started),
    });
  }
}

export function inspectE2ECommandOutput(
  target: E2ECommandTarget,
  output: { code: number; stdout: string; stderr?: string; durationMs?: number },
): E2ECommandResult {
  const text = stripAnsi(`${output.stdout}\n${output.stderr ?? ""}`);
  const missing = (target.required ?? []).filter((token) => !text.includes(token));
  const forbiddenMatches = (target.forbidden ?? defaultForbiddenTokens).filter((token) => text.includes(token));
  return {
    ...target,
    code: output.code,
    passed: output.code === 0 && missing.length === 0 && forbiddenMatches.length === 0,
    durationMs: output.durationMs ?? 0,
    missing,
    forbiddenMatches,
    outputPreview: previewText(text),
  };
}

export async function inspectE2EArtifactTarget(
  target: E2EArtifactTarget,
  options: { cwd?: string } = {},
): Promise<E2EArtifactResult> {
  const path = options.cwd ? `${options.cwd.replace(/\/$/, "")}/${target.path}` : target.path;
  try {
    const data = await Deno.readFile(path);
    const text = isLikelyText(target.path) ? new TextDecoder().decode(data) : "";
    return inspectE2EArtifactData(target, data.byteLength, text);
  } catch (error) {
    return {
      ...target,
      passed: false,
      bytes: 0,
      missing: [...(target.required ?? [])],
      forbiddenMatches: [],
      error: error instanceof Error ? error.message : String(error),
    };
  }
}

export function inspectE2EArtifactData(target: E2EArtifactTarget, bytes: number, text = ""): E2EArtifactResult {
  const missing = (target.required ?? []).filter((token) => !text.includes(token));
  const forbiddenMatches = (target.forbidden ?? defaultForbiddenTokens).filter((token) => text.includes(token));
  const tooSmall = bytes < (target.minBytes ?? 1);
  return {
    ...target,
    passed: !tooSmall && missing.length === 0 && forbiddenMatches.length === 0,
    bytes,
    missing: tooSmall ? [`minBytes:${target.minBytes ?? 1}`, ...missing] : missing,
    forbiddenMatches,
  };
}

export function formatE2EReport(report: E2EReport): string {
  const commandRows = report.commands.map((result) =>
    `| ${result.passed ? "pass" : "fail"} | ${result.id} | ${result.durationMs} | ${result.code} | ${
      result.missing.join(", ") || "-"
    } | ${result.forbiddenMatches.join(", ") || "-"} |`
  );
  const artifactRows = report.artifacts.map((result) =>
    `| ${result.passed ? "pass" : "fail"} | ${result.id} | ${result.bytes} | ${result.missing.join(", ") || "-"} | ${
      result.forbiddenMatches.join(", ") || "-"
    } |`
  );
  const failed = [
    ...report.commands.filter((result) => !result.passed).map((result) => ({
      label: result.label,
      preview: result.outputPreview,
    })),
    ...report.artifacts.filter((result) => !result.passed).map((result) => ({
      label: result.label,
      preview: result.error ??
        `missing: ${result.missing.join(", ") || "-"}; forbidden: ${result.forbiddenMatches.join(", ") || "-"}`,
    })),
  ];
  return [
    "# End-To-End Report",
    "",
    `Status: ${report.passed ? "pass" : "fail"}`,
    `Duration: ${report.durationMs}ms`,
    "",
    "## Commands",
    "",
    "| Status | Target | ms | Code | Missing | Forbidden |",
    "| --- | --- | ---: | ---: | --- | --- |",
    ...commandRows,
    "",
    "## Artifacts",
    "",
    "| Status | Artifact | Bytes | Missing | Forbidden |",
    "| --- | --- | ---: | --- | --- |",
    ...artifactRows,
    ...(failed.length
      ? [
        "",
        "## Failed Output Previews",
        "",
        ...failed.flatMap((failure) => [
          `### ${failure.label}`,
          "",
          "```text",
          failure.preview,
          "```",
          "",
        ]),
      ]
      : []),
  ].join("\n");
}

const defaultForbiddenTokens = [
  "ReferenceError",
  "TypeError",
  "RangeError",
  "SyntaxError",
  "error: Uncaught",
  "FAILED |",
] as const;

function isLikelyText(path: string): boolean {
  return /\.(html|js|css|json|map|txt|md)$/i.test(path);
}

function previewText(value: string): string {
  return value.split("\n").slice(0, 20).join("\n").trimEnd();
}

if (import.meta.main) {
  const json = Deno.args.includes("--json");
  const quiet = Deno.args.includes("--quiet");
  const report = await runE2ESuite();
  if (json) {
    console.log(JSON.stringify(report, null, 2));
  } else if (!quiet) {
    console.log(formatE2EReport(report));
  }
  if (!report.passed) Deno.exit(1);
}
