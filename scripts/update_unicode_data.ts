function encodeDeltaValuedRanges(
  ranges: readonly UnicodeValuedRange[],
  values: readonly string[],
  label: string,
): number[] {
  const encoded: number[] = [];
  let previousEnd = -1;
  for (let index = 0; index < ranges.length; index++) {
    const range = ranges[index];
    const valueIndex = values.indexOf(range.value);
    if (valueIndex < 0) throw new Error(`${label} range ${index} has unsupported value ${range.value}.`);
    encoded.push(range.start - previousEnd - 1, range.end - range.start, valueIndex);
    previousEnd = range.end;
  }
  return encoded;
}

function encodeDeltaBinaryRanges(ranges: readonly UnicodeCodePointRange[]): number[] {
  const encoded: number[] = [];
  let previousEnd = -1;
  for (const range of ranges) {
    encoded.push(range.start - previousEnd - 1, range.end - range.start);
    previousEnd = range.end;
  }
  return encoded;
}

