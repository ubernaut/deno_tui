function decodeDeltaRanges(
  raw: unknown,
  stride: 2 | 3,
  label: string,
): readonly UnicodeCodePointRange[] {
  if (!Array.isArray(raw) || raw.length === 0 || raw.length > 100_000 || raw.length % stride !== 0) {
    throw new Error(`Generated width ${label} encoding has an invalid length.`);
  }
  const ranges: UnicodeCodePointRange[] = [];
  let previousEnd = -1;
  for (let offset = 0; offset < raw.length; offset += stride) {
    const gap = raw[offset];
    const span = raw[offset + 1];
    if (!Number.isSafeInteger(gap) || !Number.isSafeInteger(span) || gap < 0 || span < 0) {
      throw new Error(`Generated width ${label} range ${offset / stride} is invalid.`);
    }
    if (stride === 3) {
      const valueIndex = raw[offset + 2];
      if (
        !Number.isSafeInteger(valueIndex) || valueIndex < 0 ||
        valueIndex >= EAST_ASIAN_WIDTH_VALUES.length
      ) {
        throw new Error(`Generated width ${label} value ${offset / stride} is invalid.`);
      }
    }
    const start = previousEnd + 1 + gap;
    const end = start + span;
    if (!Number.isSafeInteger(start) || !Number.isSafeInteger(end) || start < 0 || end > 0x10ffff) {
      throw new Error(`Generated width ${label} range ${offset / stride} is outside Unicode.`);
    }
    ranges.push({ start, end });
    previousEnd = end;
  }
  return ranges;
}

function rangeContains(ranges: readonly UnicodeCodePointRange[], codePoint: number): boolean {
  let low = 0;
  let high = ranges.length - 1;
  while (low <= high) {
    const middle = low + ((high - low) >>> 1);
    const range = ranges[middle];
    if (codePoint < range.start) high = middle - 1;
    else if (codePoint > range.end) low = middle + 1;
    else return true;
  }
  return false;
}

function parseCompactWidthModule(text: string): CompactWidthData {
  if (!text.startsWith(WIDTH_RUNTIME_MODULE_PREFIX) || !text.endsWith(WIDTH_RUNTIME_MODULE_SUFFIX)) {
    throw new Error("Generated compact width module wrapper is not canonical.");
  }
  const json = text.slice(WIDTH_RUNTIME_MODULE_PREFIX.length, -WIDTH_RUNTIME_MODULE_SUFFIX.length);
  let raw: unknown;
  try {
    raw = JSON.parse(json);
  } catch {
    throw new Error("Generated compact width module does not contain valid JSON data.");
  }
  if (raw === null || typeof raw !== "object" || Array.isArray(raw)) {
    throw new Error("Generated compact width data is not an object.");
  }
  const value = raw as Record<string, unknown>;
  const expectedKeys = [
    "schema",
    "schemaVersion",
    "unicodeVersion",
    "dataPackFingerprint",
    "source",
    "eastAsianWidthValues",
    "eastAsianWidthRanges",
    "assignedRanges",
    "combiningRanges",
    "zeroWidthRanges",
    "fingerprint",
  ];
  if (JSON.stringify(Object.keys(value)) !== JSON.stringify(expectedKeys)) {
    throw new Error("Generated compact width data fields are not canonical.");
  }
  if (
    value.schema !== "deno-tui.unicode-width-data" || value.schemaVersion !== 1 ||
    value.unicodeVersion !== UNICODE_VERSION ||
    typeof value.dataPackFingerprint !== "string" || !/^[0-9a-f]{64}$/.test(value.dataPackFingerprint) ||
    JSON.stringify(value.source) !== JSON.stringify(DERIVED_GENERAL_CATEGORY_SOURCE) ||
    JSON.stringify(value.eastAsianWidthValues) !== JSON.stringify(EAST_ASIAN_WIDTH_VALUES)
  ) {
    throw new Error("Generated compact width metadata is not pinned.");
  }
  decodeDeltaRanges(value.eastAsianWidthRanges, 3, "East_Asian_Width");
  const assigned = decodeDeltaRanges(value.assignedRanges, 2, "assigned");
  const combining = decodeDeltaRanges(value.combiningRanges, 2, "combining");
  const zeroWidth = decodeDeltaRanges(value.zeroWidthRanges, 2, "zero-width");
  if (
    !rangeContains(assigned, 0x0041) || !rangeContains(assigned, 0x1f600) ||
    rangeContains(assigned, 0x0378) || rangeContains(assigned, 0x2fa1e) ||
    rangeContains(assigned, 0xfdd0) || rangeContains(assigned, 0x10ffff) ||
    rangeContains(assigned, 0x2065)
  ) {
    throw new Error("Generated compact width assignment ranges fail the pinned sentinel corpus.");
  }
  if (
    !rangeContains(combining, 0x0301) || !rangeContains(combining, 0x1161) ||
    !rangeContains(combining, 0x11a8) || rangeContains(combining, 0x09be) ||
    rangeContains(combining, 0xff9e) || rangeContains(combining, 0x302e) ||
    rangeContains(combining, 0x1f3fb) || !rangeContains(zeroWidth, 0x200c) ||
    !rangeContains(zeroWidth, 0x200d)
  ) {
    throw new Error("Generated compact width zero-advance ranges fail the pinned sentinel corpus.");
  }
  const content: CompactWidthDataContent = {
    schema: value.schema,
    schemaVersion: value.schemaVersion,
    unicodeVersion: value.unicodeVersion,
    dataPackFingerprint: value.dataPackFingerprint,
    source: value.source as UnicodeDataPackSource,
    eastAsianWidthValues: value.eastAsianWidthValues as readonly string[],
    eastAsianWidthRanges: value.eastAsianWidthRanges as readonly number[],
    assignedRanges: value.assignedRanges as readonly number[],
    combiningRanges: value.combiningRanges as readonly number[],
    zeroWidthRanges: value.zeroWidthRanges as readonly number[],
  };
  if (value.fingerprint !== unicodeDataSha256(JSON.stringify(content))) {
    throw new Error("Generated compact width fingerprint does not match its content.");
  }
  return { ...content, fingerprint: value.fingerprint as string };
}

function parseIndicConjunctBreakModule(text: string): IndicConjunctBreakData {
  if (
    !text.startsWith(INDIC_CONJUNCT_BREAK_MODULE_PREFIX) ||
    !text.endsWith(INDIC_CONJUNCT_BREAK_MODULE_SUFFIX)
  ) {
    throw new Error("Generated Indic_Conjunct_Break module wrapper is not canonical.");
  }
  const json = text.slice(
    INDIC_CONJUNCT_BREAK_MODULE_PREFIX.length,
    -INDIC_CONJUNCT_BREAK_MODULE_SUFFIX.length,
  );
  let raw: unknown;
  try {
    raw = JSON.parse(json);
  } catch {
    throw new Error("Generated Indic_Conjunct_Break module does not contain valid JSON data.");
  }
  if (raw === null || typeof raw !== "object" || Array.isArray(raw)) {
    throw new Error("Generated Indic_Conjunct_Break data is not an object.");
  }
  const value = raw as Record<string, unknown>;
  if (value.unicodeVersion !== UNICODE_VERSION) {
    throw new Error("Generated Indic_Conjunct_Break Unicode version is not pinned.");
  }
  if (JSON.stringify(value.source) !== JSON.stringify(DERIVED_CORE_PROPERTIES_SOURCE)) {
    throw new Error("Generated Indic_Conjunct_Break source metadata is not pinned.");
  }
  if (!Array.isArray(value.ranges)) throw new Error("Generated Indic_Conjunct_Break ranges are missing.");
  const ranges = value.ranges as unknown[];
  let previousEnd = -1;
  for (let index = 0; index < ranges.length; index++) {
    const range = ranges[index];
    if (range === null || typeof range !== "object" || Array.isArray(range)) {
      throw new Error(`Generated Indic_Conjunct_Break range ${index} is invalid.`);
    }
    const entry = range as Record<string, unknown>;
    if (
      !Number.isInteger(entry.start) || !Number.isInteger(entry.end) ||
      (entry.start as number) < 0 || (entry.end as number) > 0x10ffff ||
      (entry.start as number) > (entry.end as number) || (entry.start as number) <= previousEnd ||
      !["Consonant", "Extend", "Linker"].includes(entry.value as string)
    ) {
      throw new Error(`Generated Indic_Conjunct_Break range ${index} is invalid.`);
    }
    previousEnd = entry.end as number;
  }
  const content = {
    unicodeVersion: value.unicodeVersion,
    source: value.source,
    ranges: value.ranges,
  };
  if (value.fingerprint !== unicodeDataSha256(JSON.stringify(content))) {
    throw new Error("Generated Indic_Conjunct_Break fingerprint does not match its content.");
  }
  return value as unknown as IndicConjunctBreakData;
}

function parseModule(text: string): UnicodeDataPack {
  if (!text.startsWith(MODULE_PREFIX) || !text.endsWith(MODULE_SUFFIX)) {
    throw new Error("Generated Unicode module wrapper is not canonical.");
  }
  const json = text.slice(MODULE_PREFIX.length, -MODULE_SUFFIX.length);
  let raw: unknown;
  try {
    raw = JSON.parse(json);
  } catch {
    throw new Error("Generated Unicode module does not contain valid JSON data.");
  }
  return validateUnicodeDataPack(raw);
}

function assertPinnedMetadata(pack: UnicodeDataPack): void {
  if (pack.unicodeVersion !== UNICODE_VERSION) throw new Error("Generated pack Unicode version is not pinned.");
  if (pack.sources.length !== SOURCES.length) throw new Error("Generated pack source count is not pinned.");
  for (let index = 0; index < SOURCES.length; index++) {
    const expected = SOURCES[index];
    const actual = pack.sources[index];
    if (actual.name !== expected.name || actual.url !== expected.url || actual.sha256 !== expected.sha256) {
      throw new Error(`Generated pack source metadata differs at index ${index}.`);
    }
  }
}

async function downloadSource(source: UnicodeDataPackSource): Promise<string> {
  const response = await fetch(source.url, { redirect: "error" });
  if (!response.ok) throw new Error(`Unicode source request failed (${response.status}): ${source.url}`);
  const contentLength = response.headers.get("content-length");
  if (contentLength !== null && Number(contentLength) > MAX_SOURCE_BYTES) {
    throw new Error(`Unicode source exceeds ${MAX_SOURCE_BYTES} bytes: ${source.url}`);
  }
  const bytes = new Uint8Array(await response.arrayBuffer());
  if (bytes.length > MAX_SOURCE_BYTES) {
    throw new Error(`Unicode source exceeds ${MAX_SOURCE_BYTES} bytes: ${source.url}`);
  }
  const digest = unicodeDataSha256(bytes);
  if (digest !== source.sha256) {
    throw new Error(
      `Unicode source SHA-256 mismatch for ${source.url}: expected ${source.sha256}, received ${digest}`,
    );
  }
  return new TextDecoder("utf-8", { fatal: true }).decode(bytes);
}

async function downloadSources(): Promise<ParsedSourceFiles> {
  const textByName = new Map<string, string>();
  for (const source of SOURCES) {
    textByName.set(source.name, await downloadSource(source));
  }
  return {
    eastAsianWidth: textByName.get("east-asian-width")!,
    emoji: textByName.get("emoji-properties")!,
    graphemeBreak: textByName.get("grapheme-break")!,
  };
}

function summarize(pack: UnicodeDataPack, moduleDigest: string): string {
  const emojiRanges = pack.tables.emoji.reduce((sum, entry) => sum + entry.ranges.length, 0);
  return [
    `Unicode ${pack.unicodeVersion}`,
    `pack ${pack.fingerprint}`,
    `module ${moduleDigest}`,
    `grapheme=${pack.tables.graphemeBreak.length}`,
    `eastAsianWidth=${pack.tables.eastAsianWidth.length}`,
    `emojiProperties=${pack.tables.emoji.length}`,
    `emojiRanges=${emojiRanges}`,
  ].join(" ");
}

function packCounts(pack: UnicodeDataPack): Readonly<Record<string, number>> {
  return {
    graphemeBreakRanges: pack.tables.graphemeBreak.length,
    eastAsianWidthRanges: pack.tables.eastAsianWidth.length,
    emojiProperties: pack.tables.emoji.length,
    emojiRanges: pack.tables.emoji.reduce((sum, entry) => sum + entry.ranges.length, 0),
  };
}

function renderDiff(previous: UnicodeDataPack | undefined, next: UnicodeDataPack): string {
  const nextCounts = packCounts(next);
  if (previous === undefined) {
    return JSON.stringify(
      {
        previous: null,
        next: { unicodeVersion: next.unicodeVersion, fingerprint: next.fingerprint, counts: nextCounts },
      },
      null,
      2,
    );
  }
  const previousCounts = packCounts(previous);
  const deltas = Object.fromEntries(
    Object.keys(nextCounts).map((key) => [key, nextCounts[key] - previousCounts[key]]),
  );
  return JSON.stringify(
    {
      previous: { unicodeVersion: previous.unicodeVersion, fingerprint: previous.fingerprint, counts: previousCounts },
      next: { unicodeVersion: next.unicodeVersion, fingerprint: next.fingerprint, counts: nextCounts },
      deltas,
      sourceMetadataChanged: JSON.stringify(previous.sources) !== JSON.stringify(next.sources),
      tablesChanged: {
        graphemeBreak: JSON.stringify(previous.tables.graphemeBreak) !== JSON.stringify(next.tables.graphemeBreak),
        eastAsianWidth: JSON.stringify(previous.tables.eastAsianWidth) !== JSON.stringify(next.tables.eastAsianWidth),
        emoji: JSON.stringify(previous.tables.emoji) !== JSON.stringify(next.tables.emoji),
      },
    },
    null,
    2,
  );
}

async function check(path?: string | URL): Promise<void> {
  const target = path ?? OUTPUT_PATH;
  const text = await Deno.readTextFile(target);
  const pack = parseModule(text);
  assertPinnedMetadata(pack);
  if (renderModule(pack) !== text) throw new Error("Generated Unicode module has deterministic formatting drift.");
  const moduleDigest = unicodeDataSha256(text);
  if (moduleDigest !== REVIEWED_MODULE_SHA256) {
    throw new Error(
      `Generated Unicode module is unreviewed: expected ${REVIEWED_MODULE_SHA256}, received ${moduleDigest}`,
    );
  }
  console.log(`Unicode data check passed: ${summarize(pack, moduleDigest)}`);

  if (path === undefined) {
    const indicText = await Deno.readTextFile(INDIC_CONJUNCT_BREAK_OUTPUT_PATH);
    const indicData = parseIndicConjunctBreakModule(indicText);
    if (renderIndicConjunctBreakModule(indicData) !== indicText) {
      throw new Error("Generated Indic_Conjunct_Break module has deterministic formatting drift.");
    }
    const indicDigest = unicodeDataSha256(indicText);
    if (indicDigest !== REVIEWED_INDIC_CONJUNCT_BREAK_MODULE_SHA256) {
      throw new Error(
        `Generated Indic_Conjunct_Break module is unreviewed: expected ${REVIEWED_INDIC_CONJUNCT_BREAK_MODULE_SHA256}, received ${indicDigest}`,
      );
    }
    const graphemeBreakTest = await Deno.readFile(GRAPHEME_BREAK_TEST_OUTPUT_PATH);
    const testDigest = unicodeDataSha256(graphemeBreakTest);
    if (testDigest !== GRAPHEME_BREAK_TEST_SOURCE.sha256) {
      throw new Error(
        `GraphemeBreakTest fixture is unreviewed: expected ${GRAPHEME_BREAK_TEST_SOURCE.sha256}, received ${testDigest}`,
      );
    }
    const testText = new TextDecoder("utf-8", { fatal: true }).decode(graphemeBreakTest);
    if (!testText.startsWith(`# GraphemeBreakTest-${UNICODE_VERSION}.txt\n`)) {
      throw new Error("GraphemeBreakTest fixture version header is not pinned.");
    }
    const compactData = buildCompactGraphemeData(pack, indicData);
    const expectedCompactText = renderCompactGraphemeModule(compactData);
    const compactText = await Deno.readTextFile(GRAPHEME_RUNTIME_OUTPUT_PATH);
    if (compactText !== expectedCompactText) {
      throw new Error("Generated compact grapheme module differs from the pinned full Unicode data.");
    }
    const compactDigest = unicodeDataSha256(compactText);
    if (compactDigest !== REVIEWED_GRAPHEME_RUNTIME_MODULE_SHA256) {
      throw new Error(
        `Generated compact grapheme module is unreviewed: expected ${REVIEWED_GRAPHEME_RUNTIME_MODULE_SHA256}, ` +
          `received ${compactDigest}`,
      );
    }
    console.log(
      `Unicode grapheme data check passed: Unicode ${UNICODE_VERSION} InCB ${indicData.ranges.length} ranges, ` +
        `compact ${compactText.length} bytes ${compactDigest}, GraphemeBreakTest ${testDigest}`,
    );

    const widthText = await Deno.readTextFile(WIDTH_RUNTIME_OUTPUT_PATH);
    const widthData = parseCompactWidthModule(widthText);
    if (widthData.dataPackFingerprint !== pack.fingerprint) {
      throw new Error("Generated compact width data does not reference the pinned full Unicode pack.");
    }
    const widthCategories: WidthCategoryRanges = {
      assigned: decodeDeltaRanges(widthData.assignedRanges, 2, "assigned"),
      combining: decodeDeltaRanges(widthData.combiningRanges, 2, "combining"),
      zeroWidth: decodeDeltaRanges(widthData.zeroWidthRanges, 2, "zero-width"),
    };
    const expectedWidthData = buildCompactWidthData(pack, widthCategories);
    if (JSON.stringify(widthData) !== JSON.stringify(expectedWidthData)) {
      throw new Error("Generated compact width module differs from the pinned full Unicode data.");
    }
    if (renderCompactWidthModule(widthData) !== widthText) {
      throw new Error("Generated compact width module has deterministic formatting drift.");
    }
    const widthDigest = unicodeDataSha256(widthText);
    if (widthDigest !== REVIEWED_WIDTH_RUNTIME_MODULE_SHA256) {
      throw new Error(
        `Generated compact width module is unreviewed: expected ${REVIEWED_WIDTH_RUNTIME_MODULE_SHA256}, ` +
          `received ${widthDigest}`,
      );
    }
    console.log(
      `Unicode width data check passed: Unicode ${UNICODE_VERSION} assigned ${widthCategories.assigned.length} ranges, ` +
        `compact ${widthText.length} bytes ${widthDigest}`,
    );
  }
}

async function update(): Promise<void> {
  let previous: UnicodeDataPack | undefined;
  try {
    previous = parseModule(await Deno.readTextFile(OUTPUT_PATH));
  } catch (error) {
    if (!(error instanceof Deno.errors.NotFound)) throw error;
  }
  const [sourceFiles, derivedCoreProperties, derivedGeneralCategory, graphemeBreakTest] = await Promise.all([
    downloadSources(),
    downloadSource(DERIVED_CORE_PROPERTIES_SOURCE),
    downloadSource(DERIVED_GENERAL_CATEGORY_SOURCE),
    downloadSource(GRAPHEME_BREAK_TEST_SOURCE),
  ]);
  const pack = buildPack(sourceFiles);
  const indicData = buildIndicConjunctBreakData(derivedCoreProperties);
  const widthCategories = buildWidthCategoryRanges(pack, derivedGeneralCategory);
  assertPinnedMetadata(pack);
  const text = renderModule(pack);
  const compactData = buildCompactGraphemeData(pack, indicData);
  const compactText = renderCompactGraphemeModule(compactData);
  const widthData = buildCompactWidthData(pack, widthCategories);
  const widthText = renderCompactWidthModule(widthData);
  console.log(`Unicode data diff:\n${renderDiff(previous, pack)}`);
  await Deno.mkdir(new URL("../src/unicode/generated/", import.meta.url), { recursive: true });
  await Deno.writeTextFile(OUTPUT_PATH, text);
  const indicText = renderIndicConjunctBreakModule(indicData);
  await Deno.writeTextFile(INDIC_CONJUNCT_BREAK_OUTPUT_PATH, indicText);
  await Deno.writeTextFile(GRAPHEME_RUNTIME_OUTPUT_PATH, compactText);
  await Deno.writeTextFile(WIDTH_RUNTIME_OUTPUT_PATH, widthText);
  await Deno.mkdir(new URL("../tests/fixtures/unicode/", import.meta.url), { recursive: true });
  await Deno.writeTextFile(GRAPHEME_BREAK_TEST_OUTPUT_PATH, graphemeBreakTest);
  const moduleDigest = unicodeDataSha256(text);
  console.log(`Unicode data generated: ${summarize(pack, moduleDigest)}`);
  if (moduleDigest !== REVIEWED_MODULE_SHA256) {
    throw new Error(
      `Generated output is intentionally unreviewed. Inspect it, update REVIEWED_MODULE_SHA256 to ${moduleDigest}, then run --check.`,
    );
  }
  const indicDigest = unicodeDataSha256(indicText);
  console.log(
    `Unicode grapheme data generated: InCB ${indicData.ranges.length} ranges module ${indicDigest}, ` +
      `GraphemeBreakTest ${GRAPHEME_BREAK_TEST_SOURCE.sha256}`,
  );
  if (indicDigest !== REVIEWED_INDIC_CONJUNCT_BREAK_MODULE_SHA256) {
    throw new Error(
      `Generated Indic_Conjunct_Break output is intentionally unreviewed. Inspect it, update ` +
        `REVIEWED_INDIC_CONJUNCT_BREAK_MODULE_SHA256 to ${indicDigest}, then run --check.`,
    );
  }
  const compactDigest = unicodeDataSha256(compactText);
  console.log(`Unicode compact grapheme data generated: ${compactText.length} bytes module ${compactDigest}`);
  if (compactDigest !== REVIEWED_GRAPHEME_RUNTIME_MODULE_SHA256) {
    throw new Error(
      `Generated compact grapheme output is intentionally unreviewed. Inspect it, update ` +
        `REVIEWED_GRAPHEME_RUNTIME_MODULE_SHA256 to ${compactDigest}, then run --check.`,
    );
  }
  const widthDigest = unicodeDataSha256(widthText);
  console.log(
    `Unicode compact width data generated: assigned ${widthCategories.assigned.length} ranges, ` +
      `${widthText.length} bytes module ${widthDigest}`,
  );
  if (widthDigest !== REVIEWED_WIDTH_RUNTIME_MODULE_SHA256) {
    throw new Error(
      `Generated compact width output is intentionally unreviewed. Inspect it, update ` +
        `REVIEWED_WIDTH_RUNTIME_MODULE_SHA256 to ${widthDigest}, then run --check.`,
    );
  }
}

function usage(): never {
  console.error("Usage: deno run [permissions] scripts/update_unicode_data.ts --check [generated-module] | --update");
  Deno.exit(2);
}

if (import.meta.main) {
  if (Deno.args[0] === "--check" && Deno.args.length <= 2) await check(Deno.args[1]);
  else if (Deno.args[0] === "--update" && Deno.args.length === 1) await update();
  else usage();
}
