function compareText(left: string, right: string): number {
  return left < right ? -1 : left > right ? 1 : 0;
}

function parseCodePointRange(raw: string, source: string, lineNumber: number): UnicodeCodePointRange {
  const parts = raw.trim().split("..");
  if (parts.length < 1 || parts.length > 2 || parts.some((part) => !/^[0-9A-F]{4,6}$/.test(part))) {
    throw new Error(`${source}:${lineNumber}: malformed code-point range`);
  }
  const start = Number.parseInt(parts[0], 16);
  const end = Number.parseInt(parts[1] ?? parts[0], 16);
  if (start > end || end > 0x10ffff) throw new Error(`${source}:${lineNumber}: invalid code-point range`);
  return { start, end };
}

function dataLines(text: string, source: string): readonly [UnicodeCodePointRange, string][] {
  const result: [UnicodeCodePointRange, string][] = [];
  const lines = text.replace(/^\uFEFF/, "").split(/\r?\n/);
  for (let index = 0; index < lines.length; index++) {
    const body = lines[index].split("#", 1)[0].trim();
    if (body.length === 0) continue;
    const fields = body.split(";");
    if (fields.length !== 2) throw new Error(`${source}:${index + 1}: expected exactly two fields`);
    const property = fields[1].trim();
    if (!/^[A-Za-z][A-Za-z0-9_]{0,63}$/.test(property)) {
      throw new Error(`${source}:${index + 1}: malformed property token`);
    }
    result.push([parseCodePointRange(fields[0], source, index + 1), property]);
  }
  return result;
}

function parseValuedRanges(text: string, source: string): readonly UnicodeValuedRange[] {
  const entries = dataLines(text, source)
    .map(([range, value]) => ({ ...range, value }))
    .sort((left, right) => left.start - right.start || left.end - right.end || compareText(left.value, right.value));
  const result: UnicodeValuedRange[] = [];
  for (const entry of entries) {
    const previous = result.at(-1);
    if (previous !== undefined && entry.start <= previous.end) {
      throw new Error(`${source}: overlapping ranges at U+${entry.start.toString(16).toUpperCase()}`);
    }
    if (previous !== undefined && previous.value === entry.value && previous.end + 1 === entry.start) {
      result[result.length - 1] = { start: previous.start, end: entry.end, value: entry.value };
    } else {
      result.push(entry);
    }
  }
  return result;
}

function parseBinaryProperties(text: string, source: string): readonly UnicodeBinaryPropertyRanges[] {
  const groups = new Map<string, UnicodeCodePointRange[]>();
  for (const [range, property] of dataLines(text, source)) {
    const ranges = groups.get(property) ?? [];
    ranges.push(range);
    groups.set(property, ranges);
  }
  return Array.from(groups.entries())
    .sort(([left], [right]) => compareText(left, right))
    .map(([property, unsortedRanges]) => {
      unsortedRanges.sort((left, right) => left.start - right.start || left.end - right.end);
      const ranges: UnicodeCodePointRange[] = [];
      for (const range of unsortedRanges) {
        const previous = ranges.at(-1);
        if (previous !== undefined && range.start <= previous.end) {
          throw new Error(`${source}/${property}: overlapping ranges at U+${range.start.toString(16).toUpperCase()}`);
        }
        if (previous !== undefined && previous.end + 1 === range.start) {
          ranges[ranges.length - 1] = { start: previous.start, end: range.end };
        } else {
          ranges.push(range);
        }
      }
      return { property, ranges };
    });
}

function mergeBinaryRanges(
  input: readonly UnicodeCodePointRange[],
  label: string,
): readonly UnicodeCodePointRange[] {
  const sorted = input.map((range) => ({ ...range })).sort((left, right) =>
    left.start - right.start || left.end - right.end
  );
  const merged: UnicodeCodePointRange[] = [];
  for (const range of sorted) {
    if (range.start < 0 || range.start > range.end || range.end > 0x10ffff) {
      throw new Error(`${label}: invalid range`);
    }
    const previous = merged.at(-1);
    if (previous !== undefined && range.start <= previous.end + 1) {
      merged[merged.length - 1] = { start: previous.start, end: Math.max(previous.end, range.end) };
    } else {
      merged.push(range);
    }
  }
  return merged;
}

function intersectBinaryRanges(
  left: readonly UnicodeCodePointRange[],
  right: readonly UnicodeCodePointRange[],
): readonly UnicodeCodePointRange[] {
  const result: UnicodeCodePointRange[] = [];
  let leftIndex = 0;
  let rightIndex = 0;
  while (leftIndex < left.length && rightIndex < right.length) {
    const leftRange = left[leftIndex];
    const rightRange = right[rightIndex];
    const start = Math.max(leftRange.start, rightRange.start);
    const end = Math.min(leftRange.end, rightRange.end);
    if (start <= end) result.push({ start, end });
    if (leftRange.end < rightRange.end) leftIndex += 1;
    else rightIndex += 1;
  }
  return result;
}

function buildWidthCategoryRanges(
  pack: UnicodeDataPack,
  generalCategoryText: string,
): WidthCategoryRanges {
  if (!generalCategoryText.replace(/^\uFEFF/, "").startsWith(`# DerivedGeneralCategory-${UNICODE_VERSION}.txt\n`)) {
    throw new Error("DerivedGeneralCategory.txt: Unicode version header is not pinned");
  }
  const generalCategories = parseValuedRanges(generalCategoryText, "DerivedGeneralCategory.txt");
  if (
    generalCategories.length === 0 || generalCategories[0].start !== 0 ||
    generalCategories.at(-1)?.end !== 0x10ffff
  ) {
    throw new Error("DerivedGeneralCategory.txt: category ranges do not cover Unicode");
  }
  for (let index = 1; index < generalCategories.length; index += 1) {
    if (generalCategories[index].start !== generalCategories[index - 1].end + 1) {
      throw new Error("DerivedGeneralCategory.txt: category ranges contain a gap");
    }
  }

  const assigned = mergeBinaryRanges(
    generalCategories.filter((range) => range.value !== "Cn"),
    "DerivedGeneralCategory assigned",
  );
  const nonspacingMarks = generalCategories.filter((range) => range.value === "Mn" || range.value === "Me");
  const hangulContinuations = pack.tables.graphemeBreak.filter((range) => range.value === "V" || range.value === "T");
  const combining = mergeBinaryRanges(
    [...nonspacingMarks, ...hangulContinuations],
    "terminal combining",
  );
  const controls = pack.tables.graphemeBreak.filter((range) =>
    range.value === "Control" || range.value === "CR" || range.value === "LF" || range.value === "ZWJ"
  );
  const extend = pack.tables.graphemeBreak.filter((range) => range.value === "Extend");
  const formats = generalCategories.filter((range) => range.value === "Cf");
  const zeroWidth = mergeBinaryRanges(
    [...controls, ...intersectBinaryRanges(extend, formats)],
    "terminal fixed zero-width",
  );

  if (
    !rangeContains(assigned, 0x0041) || !rangeContains(assigned, 0x1f600) ||
    rangeContains(assigned, 0x0378) || rangeContains(assigned, 0xfdd0) ||
    rangeContains(assigned, 0x10ffff) || rangeContains(assigned, 0x2065)
  ) {
    throw new Error("DerivedGeneralCategory.txt: assignment sentinel corpus failed");
  }
  if (
    !rangeContains(combining, 0x0301) || !rangeContains(combining, 0x1161) ||
    !rangeContains(combining, 0x11a8) || rangeContains(combining, 0x09be) ||
    rangeContains(combining, 0xff9e) || rangeContains(combining, 0x302e) ||
    rangeContains(combining, 0x1f3fb)
  ) {
    throw new Error("DerivedGeneralCategory.txt: combining sentinel corpus failed");
  }
  if (!rangeContains(zeroWidth, 0x200c) || !rangeContains(zeroWidth, 0x200d)) {
    throw new Error("DerivedGeneralCategory.txt: fixed zero-width sentinel corpus failed");
  }
  return { assigned, combining, zeroWidth };
}

function parseIndicConjunctBreakRanges(text: string): readonly UnicodeValuedRange[] {
  const entries: UnicodeValuedRange[] = [];
  const lines = text.replace(/^\uFEFF/, "").split(/\r?\n/);
  for (let index = 0; index < lines.length; index++) {
    const body = lines[index].split("#", 1)[0].trim();
    if (body.length === 0) continue;
    const fields = body.split(";").map((field) => field.trim());
    if (fields.length < 2 || fields[1] !== "InCB") continue;
    if (fields.length !== 3 || !["Consonant", "Extend", "Linker"].includes(fields[2])) {
      throw new Error(`DerivedCoreProperties.txt:${index + 1}: malformed Indic_Conjunct_Break record`);
    }
    entries.push({
      ...parseCodePointRange(fields[0], "DerivedCoreProperties.txt", index + 1),
      value: fields[2],
    });
  }
  entries.sort((left, right) =>
    left.start - right.start || left.end - right.end || compareText(left.value, right.value)
  );
  const result: UnicodeValuedRange[] = [];
  for (const entry of entries) {
    const previous = result.at(-1);
    if (previous !== undefined && entry.start <= previous.end) {
      throw new Error(
        `DerivedCoreProperties.txt: overlapping Indic_Conjunct_Break ranges at U+${
          entry.start.toString(16).toUpperCase()
        }`,
      );
    }
    if (previous !== undefined && previous.value === entry.value && previous.end + 1 === entry.start) {
      result[result.length - 1] = { start: previous.start, end: entry.end, value: entry.value };
    } else {
      result.push(entry);
    }
  }
  if (!result.some((entry) => entry.value === "Consonant")) {
    throw new Error("DerivedCoreProperties.txt: Indic_Conjunct_Break Consonant data is missing");
  }
  if (!result.some((entry) => entry.value === "Linker")) {
    throw new Error("DerivedCoreProperties.txt: Indic_Conjunct_Break Linker data is missing");
  }
  return result;
}

